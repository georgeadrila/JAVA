package Day_Five;

public class Mathematical_funcs {
    public static void main(String[] args) {
        //  use of java  mathematical functions
        System.out.println(Math.abs(-5.5)); // absolute value
        System.out.println(Math.ceil(5.5)); // ceiling value
        System.out.println(Math.floor(5.5)); // floor value
        System.out.println(Math.max(5, 10)); // maximum value
        System.out.println(Math.min(5, 10)); // minimum value
        System.out.println(Math.pow(5, 2)); // power value
        System.out.println(Math.sqrt(25)); // square root value
        System.out.println(Math.random()); // random value

        //  use of java  mathematical constants
        System.out.println(Math.PI); // pi value
        System.out.println(Math.E); // e value

        //  use of java  mathematical functions
        System.out.println(Math.toDegrees(1.57)); // convert radians to degrees
        System.out.println(Math.toRadians(90)); // convert degrees to radians

    }
}

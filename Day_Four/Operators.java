package Day_Four;

public class Operators {
    public static void main(String[] args) {
        float a = 10.5f, b = 5.5f;
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
    }
}
